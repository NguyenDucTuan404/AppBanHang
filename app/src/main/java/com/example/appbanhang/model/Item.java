package com.example.appbanhang.model;

public class Item {
    int idsp;
    String tensp;
    int soluong;
    String hinhhanh;

    public int getIdsp() {
        return idsp;
    }

    public void setIdsp(int idsp) {
        this.idsp = idsp;
    }

    public String getTensp() {
        return tensp;
    }

    public void setTensp(String tensp) {
        this.tensp = tensp;
    }

    public int getSoluong() {
        return soluong;
    }

    public void setSoluong(int soluong) {
        this.soluong = soluong;
    }

    public String getHinhhanh() {
        return hinhhanh;
    }

    public void setHinhhanh(String hinhhanh) {
        this.hinhhanh = hinhhanh;
    }
}
